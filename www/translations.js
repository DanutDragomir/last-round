function t(key, vars) {
  const lang = window._lang || 'en';
  const str = (window.TRANSLATIONS[lang] && window.TRANSLATIONS[lang][key])
              || (window.TRANSLATIONS['en'] && window.TRANSLATIONS['en'][key])
              || key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : '{' + k + '}'));
}

function setLang(code) {
  window._lang = code;
  localStorage.setItem('lr_lang', code);
  document.documentElement.lang = code;
  document.documentElement.dir = code === 'ar' ? 'rtl' : 'ltr';
  applyI18n();
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
}

function initLang() {
  const saved = localStorage.getItem('lr_lang') || 'en';
  window._lang = saved;
  document.documentElement.lang = saved;
  document.documentElement.dir = saved === 'ar' ? 'rtl' : 'ltr';
}

